#include "philo.h"

unsigned long	get_time(void)
{
	struct timeval	time;

	gettimeofday(&time, NULL);
	return ((time.tv_sec * 1000) + (time.tv_usec / 1000));
}

void	ft_usleep(unsigned long time)
{
	unsigned long	start_time;

	start_time = get_time();
	while (get_time() - start_time < time)
		usleep(100);
}
